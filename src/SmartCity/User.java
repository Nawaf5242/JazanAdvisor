    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String Type=cb1.getSelectedItem().toString();
       String NameOfPlace=t1.getText();
       String Adress=t2.getText();
       String WorkingHour=cb2.getSelectedItem().toString();
        long contactnumber= Long.parseLong(t3.getText());
        
        try{
           Connection con=MyConnection.getConnection();
           PreparedStatement pst=con.prepareStatement("insert into student(Type, NameOfPlace, Adress, WorkingHour, ContactNumber)values(?,?,?,?,?)");
           pst.setString(1, Type);
           pst.setString(2, NameOfPlace);
           pst.setString(3, Adress);
           pst.setString(4, WorkingHour);
           pst.setLong(5, contactnumber);
            int ok=JOptionPane.showConfirmDialog(rootPane,"Confirm","Do you want to save",JOptionPane.YES_NO_CANCEL_OPTION);
            if(ok==0){
                pst.execute();
                JOptionPane.showMessageDialog(rootPane, "Data stored succesful","Done",1);
            }//if close
        }//try close
        catch(Exception ex)
        {System.out.println(ex);}
    }//GEN-LAST:event_jButton1ActionPerformed

